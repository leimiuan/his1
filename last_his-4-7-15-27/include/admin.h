// [核心架构说明]：
// 本文件为 HIS 系统“管理与决策支持子系统 (Admin Subsystem)”的接口声明头文件。
// 暴露了全院人事管理、财务审计、药房进销存、排班调度以及诊疗记录核心 CRUD 的 API。
// 本次更新严格保留了原有的所有函数声明，并补全了企业级的中文架构注释。

#ifndef ADMIN_H
#define ADMIN_H

#include "common.h"

/* ==========================================================================
 * 核心子系统入口 (Admin Subsystem Entry)
 * ========================================================================== */
void admin_subsystem();

/* ==========================================================================
 * 诊疗记录核心维护 (Record CRUD)
 * 包含了急救绿色通道（Score 999强制插队）和双医生责任绑定的底层支持
 * ========================================================================== */
void add_record_interactive();        // 新增诊疗记录（支持识别“救护车”触发置顶）
void import_records_from_file();      // 批量导入历史诊疗数据（支持容错与脏数据跳过）
void modify_record_with_revoke();     // 修改诊疗记录（采用金融级撤销后补录策略，留存审计痕迹）
void delete_record_with_revoke();     // 逻辑删除诊疗记录（标记为 -1 撤销状态，绝不物理删除）

/* ==========================================================================
 * 核心业务检索与多维查询 (Queries & Schedulers)
 * ========================================================================== */
void query_records_by_department();      // 按科室维度检索诊疗记录
void query_records_by_doctor();          // 按主治医生工号精准溯源历史诊疗记录
void query_records_by_patient_keyword(); // 按患者编号或姓名模糊匹配全生命周期记录
void query_records_in_time_range();      // 按自定义时间范围提取诊疗/手术流水
void manage_duty_schedule();             // 全院医护人员急诊/门诊值班排班调度中心

/* ==========================================================================
 * 医院人事与权限管理 (Human Resources)
 * ========================================================================== */
void register_doctor();               // 注册新医生档案（含 1-4 级手术权限/职称强制分配）
void delete_doctor();                 // 吊销/注销医生系统执业账号

/* ==========================================================================
 * 药房进销存物流系统 (Pharmacy & Logistics)
 * ========================================================================== */
void add_new_medicine();              // 新增药品品规档案
void medicine_stock_in();             // 药品批量入库登记
void view_inventory_report();         // 全局药房库存深度审计（含低于警戒线的低库存告警）

/* ==========================================================================
 * 大数据分析与财务报表 (Reports and Analytics)
 * ========================================================================== */
void view_hospital_financial_report(); // 全局财务与医保结算报告（含住院患者欠费风险深度扫描）
void analyze_historical_trends();      // 各科室就诊流量趋势与负载分析
void optimize_bed_allocation();        // 全院各病区床位资源实时占用率动态监控看板

#endif /* ADMIN_H */