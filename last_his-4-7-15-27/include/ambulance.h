// [核心架构说明]：
// 本文件为 HIS 系统“120院前急救与救护车调度子系统 (Ambulance Subsystem)”的接口契约。
// 核心职责：
// 1. 打破传统 HIS 的院内边界，将院前急救、车辆调度与院内急诊、手术室资源进行深度物理级绑定；
// 2. 对外暴露急救大屏控制台入口以及底层的出勤日志防呆追踪 API；
// 3. 满足系统对于高并发抢救场景下的资源锁定与优先级置顶（Score 999 机制）的前置声明需求。
//
// 本次更新严格保留了所有原始声明，并完成了企业级架构注释的规范化对齐。

#ifndef AMBULANCE_H
#define AMBULANCE_H

#include "common.h"

/* ==========================================================================
 * 120 院前急救与救护车调度子系统控制台入口
 * ========================================================================== */

/**
 * @brief 120 救护车智慧调度子系统主入口 (UI & Event Loop)
 * * * 提供交互式的大屏看板菜单，用于对全院救护车队进行三态（空闲/出车/消毒）管理。
 * * 【跨模块神级联动】：
 * 1. 车辆回场联动：调度员确认车辆回场时，可一键将拉回的危重患者转入【急诊绿色通道】，
 * 系统将自动跨模块调用挂号引擎生成 `RECORD_EMERGENCY` 急诊单，并在医生端大屏【置顶亮红灯】。
 * 2. 手术室资源联动：派车前可提前透视全局手术室（Operating Room）的实时状态，为大抢救预留生命通道。
 */
void ambulance_subsystem();

/* ==========================================================================
 * 底层审计与日志钩子接口 (Audit & Logging Hooks)
 * ========================================================================== */

/**
 * @brief 生成并写入救护车出勤日志 (底层数据防呆追踪接口)
 * * @param vehicle_id  执行本次派发任务的车辆唯一编号
 * @param destination 救护车前往的现场目的地或 GPS 坐标
 * * * 【核心审计要求落实】：
 * 用于在急救中心（admin端）或患者自助服务大厅（patient端）一键呼叫 120 时被系统底层自动静默调用。
 * 该函数会抓取精准的秒级时间戳 (YYYY-MM-DD HH:MM:SS)，并在关机时被 `data_manager.c` 
 * 强制落盘序列化至 `ambulance_logs.txt`，形成不可篡改、100% 可追溯的历史出勤流水。
 */
void add_ambulance_log(const char* vehicle_id, const char* destination);

#endif // AMBULANCE_H