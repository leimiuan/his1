#ifndef UTILS_H
#define UTILS_H

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <time.h>

/* ==========================================
 * 底层安全工具与 UI 渲染模块接口契约
 * 适配版本：智慧医院 HIS v4.0 终极重构版 (含荣誉班进阶特性)
 * 【系统定位】：承载全院系统的非法输入拦截、缓冲区溢出防御以及终端大屏 UI 绘制。
 * ========================================== */

/* ==========================================
 * 1. UI 美化与控制台颜色宏定义 (ANSI 转义序列)
 * 目标：提供多级视觉反馈，满足题签中“清晰美观的输出方式”重点要求
 * ========================================== */
#ifndef COLOR_RESET
#define COLOR_RESET   "\033[0m"
#define COLOR_RED     "\033[1;31m"    // 视觉反馈：用于错误提示、余额不足、床位熔断告警、急诊优先级标记
#define COLOR_GREEN   "\033[1;32m"    // 视觉反馈：用于支付成功、挂号完成、救护车出发、系统正常状态
#define COLOR_YELLOW  "\033[1;33m"    // 视觉反馈：用于重要录入提示、待处理状态、资源紧张预警
#define COLOR_BLUE    "\033[1;34m"    // 视觉反馈：用于系统主标题、电子发票边框、门诊常规状态
#define COLOR_CYAN    "\033[1;36m"    // 视觉反馈：用于排队信息、进度条、AI 智能推荐信息
#endif

/* ==========================================
 * 2. 防御性输入接口 (Robust Input Handling)
 * 【重点考察响应】：彻底杜绝 scanf 导致的非法输入死循环，完美防御缓冲区溢出攻击。
 * ========================================== */

/**
 * @brief 清空标准输入缓冲区 (stdin)
 * 移除残余的换行符和无效字符，防止上一次的错误输入干扰后续读取逻辑。
 */
void clear_input_buffer();

/**
 * @brief 安全读取字符串 (防御缓冲区溢出)
 * 使用 fgets 替代危险的 scanf，自动剥离末尾换行符，并进行极值截断保护。
 * @param buffer 目标缓冲区指针
 * @param max_len 缓冲区物理最大长度
 */
void safe_get_string(char* buffer, int max_len);

/**
 * @brief 安全读取整数 (带极值拦截与乱码过滤)
 * 【重点考察响应】：具备非数字字符(如 "123abc")彻底拦截功能。
 * 【硬性指标落实】：内置极值校验，确保“最大数量不超过 int 允许最大整数 (INT_MAX)”。
 * @return 转换后绝对合法的整型数值
 */
int safe_get_int();

/**
 * @brief 安全读取双精度浮点数 (专用于严苛的财务流转)
 * 【重点考察响应】：确保金额精确到元、角、分，彻底过滤非法字符。
 * 【硬性指标落实】：内置财务合规边界锁，强制规定“单笔金额最高额度不超过 10 万”且绝不允许为负数。
 * @return 转换后绝对合法的双精度财务数值
 */
double safe_get_double();

/**
 * @brief 暂停程序执行，驻留当前屏幕数据
 * 在屏幕显示“请按回车键继续...”，常用于展示长列表或财务报表后，提供人性化的阅读时间。
 */
void wait_for_enter();

/* ==========================================
 * 3. 界面渲染与系统辅助工具
 * ========================================== */

/**
 * @brief 打印带装饰线的模块大屏幕头部
 * @param title 模块标题 (如 "120 救护车调度中心")
 */
void print_header(const char* title);

/**
 * @brief 打印标准化的列表隔离线
 */
void print_divider();

/**
 * @brief 模拟系统加载进度条动画
 * 用于数据持久化保存或系统初始化，增加用户交互沉浸感与系统厚重感。
 * @param task_name 当前执行的底层任务描述
 */
void show_progress_bar(const char* task_name);

/**
 * @brief 获取符合系统持久化规范的日期字符串 
 * 格式要求：强制格式化为 "YYYY-MM-DD"。
 * 【注意】：严禁包含空格或特殊字符，以确保 data_manager 底层文件解析引擎的高效运转。
 * @param time_buffer 接收日期的缓冲区 (建议长度 >= 32)
 */
void get_current_time_str(char* time_buffer);

/* ==========================================
 * 4. 【系统扩展】救护车与手术室高精度时间辅助集
 * ========================================== */

/**
 * @brief 获取当前系统精确时间 (秒级高精度)
 * 格式强制规范为："YYYY-MM-DD HH:MM:SS"。
 * @param time_buffer 外部传入的缓冲区，用于接收生成的字符串 (建议分配 32 字节)
 * 【业务联动】：专为 120 救护车历史出勤表 (调度日志) 以及 手术室状态流转 (手术开始/结束) 提供秒级精准时间戳支持。
 */
void get_precise_time_str(char* time_buffer);

#endif // UTILS_H